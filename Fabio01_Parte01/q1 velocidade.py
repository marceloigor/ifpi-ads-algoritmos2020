# 1. Leia uma velocidade em m/s, calcule e escreva esta velocidade 
# em km/h. (Vkm/h = Vm/s * 3.6)

# Entrada
velocidade_ms = float(input('Digite a velocidade em m/s: '))

# Processamento
velocidade_kmh = velocidade_ms * 3.6

#Saída
print(f'{velocidade_ms} m/s é equivalente a {velocidade_kmh} km/h')
