# 11. Leia 3 números, calcule e escreva a média dos números.

# Entrada
valor1 = float(input('Digite um valor: '))
valor2 = float(input('Digite um 2º valor: '))
valor3 = float(input('Digite um 3º valor: '))

# Processamento
media = (valor1 + valor2 + valor3) / 3

# Saída
print(f'Sua média é {media:.1f}')